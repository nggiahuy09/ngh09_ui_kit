/// Primitive responsive breakpoint tokens (logical pixel widths).
abstract final class BreakpointTokens {
  /// Phones (< 600).
  static const double mobile = 600;

  /// Tablets (>= 600, < 1024).
  static const double tablet = 1024;

  /// Desktops (>= 1024).
  static const double desktop = 1440;
}
