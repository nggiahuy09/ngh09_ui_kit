/// The size of a `GHAppCheckbox`, controlling the box dimensions.
///
/// Matches the three checkbox sizes in the Finesse UI Kit:
///
/// | Finesse name | Dart value | Box     |
/// |--------------|------------|---------|
/// | Small        | [small]    | 16×16dp |
/// | Medium       | [medium]   | 20×20dp |
/// | Large        | [large]    | 24×24dp |
enum CheckboxSize {
  /// Small checkbox — 16×16dp box.
  small,

  /// Medium checkbox — 20×20dp box.
  medium,

  /// Large checkbox — 24×24dp box.
  large,
}
