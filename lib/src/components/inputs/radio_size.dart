/// The size of a `GHAppRadio`, controlling the circle dimensions.
///
/// Matches the three radio sizes in the Finesse UI Kit:
///
/// | Finesse name | Dart value | Circle  | Dot   |
/// |--------------|------------|---------|-------|
/// | Small        | [small]    | 16×16dp | 6dp   |
/// | Medium       | [medium]   | 20×20dp | 8dp   |
/// | Large        | [large]    | 24×24dp | 10dp  |
enum RadioSize {
  /// Small radio — 16×16dp circle, 6dp dot.
  small,

  /// Medium radio — 20×20dp circle, 8dp dot.
  medium,

  /// Large radio — 24×24dp circle, 10dp dot.
  large,
}
